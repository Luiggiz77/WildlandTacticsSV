/// <summary>
/// Indica el tipo de objeto que es.
/// </summary>
public enum PurchasableCategory
{
    /// <summary>
    /// Indica que el objeto a comprar es un item de juego
    /// </summary>
    GameplayItem = 0,

    /// <summary>
    /// Indica que es el objeto a comprar es un color
    /// </summary>
    Color = 1,

    /// <summary>
    /// Indica que el objeto a comprar es un skin (facción)
    /// </summary>
    Skin = 2,

    /// <summary>
    /// Indica que el objeto a comprar es una flor de ilusión (terreno)
    /// </summary>
    FlorIlusion = 3,

    /// <summary>
    /// Indica que el objeto a comprar sus bombones
    /// </summary>
    Marshmallow = 4,

    /// <summary>
    /// Indica que el objecto a comprar son hojas de árbol
    /// </summary>
    TreeLeaf = 5
}
